		<h2>About the Curaçao Rugby Federation</h2>
		<p>
			The <strong>Curaçao Rugby Federation (CRF)</strong> — <em>Federashon di Rugby di Kòrsou</em> in
			Papiamentu — is the official governing body for rugby union in Curaçao, a constituent country
			of the Kingdom of the Netherlands situated in the southern Caribbean Sea, just 65 km north of
			the Venezuelan coastline.
		</p>
		<p>
			Rugby in Curaçao has grown from a small group of enthusiasts into a proper federation with
			multiple teams, including men's and women's XVs and Sevens squads. The CRF is affiliated
			with <strong>Rugby Americas North (RAN)</strong> and the global governing body
			<strong>World Rugby</strong>.
		</p>

		<h2>The Curaçao Sharks 🦈</h2>
		<p>
			Our national rugby union team carries the proud nickname <strong>the Sharks</strong>. They
			compete in Caribbean regional competitions including the RAN Championship and the Caribbean
			Rugby Association (CRA) tournaments, taking on nations such as Trinidad & Tobago, Barbados,
			Jamaica, and other Caribbean islands.
		</p>
		<p>
			The Sharks wear the blue, red, and yellow of Curaçao — colours that reflect the island's
			flag and its vibrant spirit. Competing on the regional stage, they represent not just a
			federation, but an entire island's passion for the game.
		</p>

		<h2>Our Mission</h2>
		<ul>
			<li>Develop rugby at all levels — youth, senior, men's, women's, and Sevens</li>
			<li>Provide access to quality coaching and training facilities</li>
			<li>Represent Curaçao with pride in Caribbean and international competitions</li>
			<li>Foster a welcoming, inclusive rugby community on the island</li>
			<li>Grow the sport among Curaçao's youth through school programs</li>
		</ul>

		<h2>The Island &amp; the Sport</h2>
		<p>
			Curaçao is renowned for its stunning beaches, colourful Willemstad architecture (a UNESCO
			World Heritage Site), and warm, year-round climate. Rugby here is played against a backdrop
			unlike anywhere else in the world — trade winds, Caribbean sunshine, and a community that
			approaches everything with the island's signature warmth.
		</p>
		<p>
			Whether you're a local, an expat, or a visitor who wants to lace up for a session, you are
			always welcome on the pitch with us.
		</p>

		<h2>Affiliations</h2>
		<ul>
			<li>🌍 <strong>World Rugby</strong> — global governing body</li>
			<li>🌎 <strong>Rugby Americas North (RAN)</strong> — regional governing body</li>
			<li>🏝️ <strong>Caribbean Rugby Association (CRA)</strong> — sub-regional body</li>
		</ul>
